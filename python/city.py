city = input("Enter the name of your city: ")

if city.lower() == "kathmandu":
    print("Pasupatinath")
elif city.lower() == "pokhara":
    print("Fewa Lake")
elif city.lower() == "nepalgunj":
    print("Bageshwori Temple")
elif city.lower() == "birgunj":
    print("Ganta Ghar")
else:
    print("Please enter a valid city")
